        private Trabajador GenerarEntidad(){
        //PONER AQUI SI FALTAN DATOSSSSSSSS
        
        Trabajador trabajador = new Trabajador();
        if(!TxtIDTrabajador.getText().equals("")){
            trabajador.setExiste(true);
            trabajador.setId_trabajador(Integer.parseInt(TxtIDTrabajador.getText()));
        }
        trabajador.setId_trabajador(Integer.parseInt( TxtIDTrabajador.getText()));
        trabajador.setNombre(TxtNombre.getText());
        trabajador.setApellidos(TxtApellidos.getText());
        trabajador.setTelefono(TxtTelefono.getText());
        trabajador.setResidencia(TxtResidencia.getText());
        return trabajador;
    }
    
