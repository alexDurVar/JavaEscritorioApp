    private void TblTrabajadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblTrabajadorMouseClicked
        // TODO add your handling code here:
         try {
            LNTrabajador logica = new LNTrabajador();
            Trabajador trabajador;
            String condicion;
            
            if(evt.getClickCount() == 2){
                int fila = TblTrabajador.rowAtPoint(evt.getPoint());
                TxtIDTrabajador.setText(TblTrabajador.getValueAt(fila, 0).toString());
                condicion = String.format("id_trabajador = %s", TxtIDTrabajador.getText());
                trabajador = logica.ObtenerRegistro(condicion);
                
                TxtIDTrabajador.setText(String.valueOf(trabajador.getId_trabajador()));
                TxtNombre.setText(trabajador.getNombre());
                TxtApellidos.setText(trabajador.getApellidos());
                TxtTelefono.setText(trabajador.getTelefono());
                TxtResidencia.setText(trabajador.getResidencia());
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_TblTrabajadorMouseClicked

