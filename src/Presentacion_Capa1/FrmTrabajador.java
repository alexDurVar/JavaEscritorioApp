    private void BtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresarActionPerformed
        // TODO add your handling code here:
        
        if (TxtIDTrabajador.getText().equals("") || TxtNombre.getText().equals("") || TxtApellidos.getText().equals("") || TxtTelefono.getText().equals("") || TxtResidencia.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Faltan Datos");
        } else {
            LNTrabajador logica = new LNTrabajador();
            Trabajador trabajador = GenerarEntidad();
            try {
                if((logica.Verificar_Insert(trabajador))==1){
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                }else{
                    logica.Insertar(trabajador);
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                }
                
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }

            try {
                CargarDatos("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
        
       
    }//GEN-LAST:event_BtnIngresarActionPerformed

