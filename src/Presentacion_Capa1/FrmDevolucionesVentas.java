    private void BtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresarActionPerformed

        // TODO add your handling code here:
        if (TxtNumDev.getText().equals("") || TxtNumFact.getText().equals("")  || TxtMotivo.getText().equals("") || TxtIdTrabajador.getText().equals("") || DCFechaFact.getDate().equals("") /*|| TxtPrecioC.getText().equals("")*/ || DCFechaRealizacion.getDate().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe insertar datos");
        } else {
            LNDevolucionesVentas logica = new LNDevolucionesVentas();
            DevolucionesVentas devoluciones = GenerarEntidad();
            try {
                if((logica.VerificarIns(devoluciones))==1  || (logica.VerificarFecha(devoluciones))==1 || (logica.VerificarIng2(devoluciones))==1 ) {
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                } else if(TxtGarantia.getText().equals("No Aplica")){
                    JOptionPane.showMessageDialog(this, "Este producto no tiene garantia");
                }
               else{
                    logica.Insertar(devoluciones);
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                }
                
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }

            try {
                Limpiar();
                CargarDatos("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }

          
         
    }//GEN-LAST:event_BtnIngresarActionPerformed

