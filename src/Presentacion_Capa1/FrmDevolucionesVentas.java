    private void CargarDatos(String condicion) throws  Exception{
        try {
            LNDevolucionesVentas logica = new LNDevolucionesVentas();
            List<DevolucionesVentas> lista;
            LimpiarTabla();
            Object[] fila = new Object[8];
            lista = logica.ListarRegistros(condicion);
            for(DevolucionesVentas devoluciones:lista){
                fila[0] = devoluciones.getNumero_devolucion();
                fila[1] = devoluciones.getNumero_factura();
                fila[2] = devoluciones.getNumero_producto();
                fila[3] = devoluciones.getFecha_realizacion();
                fila[4] = devoluciones.getFecha_limite();
                fila[5] = devoluciones.getMotivo_devolucion();
                fila[6] = devoluciones.getAceptacion_devolucion();
                fila[7] = devoluciones.getId_trabajador();
                modelo.addRow(fila);
            }
            
        } catch (Exception e) {
            throw e;
        }
    }
    
