    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        // TODO add your handling code here:
        FrmBuscarDevolucion buscar = new FrmBuscarDevolucion(null, true);
        buscar.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                try {
                    int id = buscar.ObtenerId();
                    String condicion = "";
                    LNDevolucionesVentas logica = new LNDevolucionesVentas();
                    DevolucionesVentas devoluciones;
                    if (id > -1) {
                        condicion = String.format("numero_devolucion = %s", id);
                        devoluciones = logica.ObtenerRegistro(condicion);

                        TxtNumDev.setText(String.valueOf(devoluciones.getNumero_devolucion()));
                        TxtNumFact.setText(String.valueOf(devoluciones.getNumero_factura()));
                        TxtNumP.setText(String.valueOf(devoluciones.getNumero_producto()));
                        DCFechaRealizacion.setDate(java.sql.Date.valueOf(devoluciones.getFecha_realizacion().toString()));
                        DCFechaFact.setDate(java.sql.Date.valueOf(devoluciones.getFecha_limite().toString()));
                        TxtMotivo.setText(devoluciones.getMotivo_devolucion());
                        TxtAceptacion.setSelectedItem(devoluciones.getAceptacion_devolucion());
                        TxtIdTrabajador.setText(String.valueOf(devoluciones.getId_trabajador()));
                    } else {
                        Limpiar();
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        });
       buscar.setVisible(true);
    }//GEN-LAST:event_BtnBuscarActionPerformed

