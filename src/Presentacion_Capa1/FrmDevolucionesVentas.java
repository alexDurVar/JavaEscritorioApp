    private DevolucionesVentas GenerarEntidad(){
        //PONER AQUI SI FALTAN DATOSSSSSSSS
        
        Date fecha = new java.util.Date();
        Date fecha2 = new java.util.Date();
        fecha = DCFechaRealizacion.getDate();
        fecha2 = DCFechaFact.getDate();
      
        
        java.sql.Date sqlDate = new java.sql.Date(fecha.getTime());
        java.sql.Date sqlDate2 = new java.sql.Date(fecha2.getTime());
        
        DevolucionesVentas devoluciones = new DevolucionesVentas();
        if(!TxtNumDev.getText().equals("")){
            devoluciones.setExiste(true);
            devoluciones.setNumero_devolucion(Integer.parseInt(TxtNumDev.getText()));
        }
        devoluciones.setNumero_devolucion(Integer.parseInt(TxtNumDev.getText()));
        devoluciones.setNumero_factura(Integer.parseInt(TxtNumFact.getText()));
        devoluciones.setNumero_producto(Integer.parseInt(TxtNumP.getText()));
        devoluciones.setFecha_realizacion(sqlDate);
        devoluciones.setFecha_limite(sqlDate2);
        devoluciones.setMotivo_devolucion(TxtMotivo.getText());
        devoluciones.setAceptacion_devolucion(TxtAceptacion.getSelectedItem().toString());
        devoluciones.setId_trabajador(Integer.parseInt(TxtIdTrabajador.getText()));
        return devoluciones;
    }
    
