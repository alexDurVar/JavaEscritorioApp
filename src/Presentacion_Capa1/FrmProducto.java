    void calcularPrecioTotal(){
        float prec= Float.parseFloat(TxtPrecioV.getText());
        float desc = Float.parseFloat(TxtDesc.getText());
        float imp = Float.parseFloat(TxtImp.getText());
        float total =0;
        float subt =0;
        float mDesc =0;
        
        subt = (prec*imp)/100+(prec); 
        mDesc = (subt*desc)/100;
        total = subt -mDesc;
        TxtPTotal.setText("" + total);
        if (total >= 30000 ){
            TxtGrantia.setSelectedIndex(0);
        }
        else{
            TxtGrantia.setSelectedIndex(1);
        }
        
    }
    
