    private void CargarDatos(String condicion) throws  Exception{
        try {
            LNProducto logica = new LNProducto();
            List<Producto> lista;
            LimpiarTabla();
            Object[] fila = new Object[13];
            lista = logica.ListarRegistros(condicion);
            for(Producto producto:lista){
                fila[0] = producto.getNumero_producto();
                fila[1] = producto.getNombre_producto();
                fila[2] = producto.getTipo_producto();
                fila[3] = producto.getPrecio_compra();
                fila[4] = producto.getPrecio_venta();
                fila[5] = producto.getCantidad();
                fila[6] = producto.getImpuesto();
                fila[7] = producto.getDescuento();
                fila[8] = producto.getDescripcion();
                fila[9] = producto.getGarantia();
                fila[10] = producto.getDisponibilidad();
                fila[11] = producto.getPrecio_total();
                fila[12] = producto.getNum_proveedor();
                
                
                modelo.addRow(fila);
            }
            
        } catch (Exception e) {
            throw e;
        }
    }
    
