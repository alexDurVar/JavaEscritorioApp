      private Producto GenerarEntidad(){
        //PONER AQUI SI FALTAN DATOSSSSSSSS
        
        Producto producto = new Producto();
        if(!TxtNumProducto.getText().equals("")){
            producto.setExiste(true);
            producto.setNumero_producto(Integer.parseInt(TxtNumProducto.getText()));
        }
        producto.setNumero_producto(Integer.parseInt(TxtNumProducto.getText()));
        producto.setNombre_producto(TxtNombreProducto.getText());
        producto.setTipo_producto(TxtTipoP.getSelectedItem().toString());
        producto.setPrecio_compra(Float.parseFloat(TxtPrecioC.getText()));
        producto.setPrecio_venta(Float.parseFloat(TxtPrecioV.getText()));
        producto.setCantidad(Integer.parseInt(TxtCant.getText()));
        producto.setImpuesto(Float.parseFloat(TxtImp.getText()));
        producto.setDescuento(Float.parseFloat(TxtDesc.getText()));
        producto.setDescripcion(TxtDescripcion.getText());
        producto.setGarantia(TxtGrantia.getSelectedItem().toString());
        producto.setPrecio_total(Float.parseFloat(TxtPTotal.getText()));
        producto.setDisponibilidad(Txtdisponibili.getSelectedItem().toString());
        producto.setNum_proveedor(Integer.parseInt(TxtNumProv.getText()));
        
        
        
        return producto;
    }
